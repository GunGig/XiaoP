# 目标： 将每个故事模板所需要的词语提示（如“一个名词”，“一个动词”）存入一个列表中。
# 讲解：
# “如果我们有很多故事，每个故事要填的词都不一样，
# 一个个写 input() 会很麻烦。我们可以把每个故事需要的‘词语提示’也存起来！”
print("--- 升级版疯狂填词！ ---")
adj1 = input("请输入一个形容词 (如：勇敢的)：")
char_type = input("请输入一个人物类型 (如：公主、巫师)：")
char_name = input("请输入一个人物名字：")
adj2 = input("再请输入一个形容词 (如：魔法的)：")
place = input("请输入一个地点 (如：森林)：")
verb = input("请输入一个动词 (如：寻找)：")
noun = input("请输入一个名词 (如：宝藏)：")

story = f"很久很久以前，有一位{adj1}的{char_type}，名叫{char_name}。Ta住在一个{adj2}的{place}。有一天，Ta决定去{verb}一个神秘的{noun}。"

print("\n--- 你的精彩故事 ---")
print(story)
print("--------------------")